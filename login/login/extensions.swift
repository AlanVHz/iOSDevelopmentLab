import Foundation
import UIKit

extension UIColor{
    
    // funcion de clase - significa que se puede usar sin necesidad de ser instanciada
    class func myNavBarBackground() -> UIColor {
        return UIColor(red:0.13, green:0.19, blue:0.25, alpha:1.0)
    }
    
    class func buttonsColor() -> UIColor {
        return UIColor(red:0.77, green:0.94, blue:0.97, alpha:1.0)
    }
    
}

